package com.example.studentmanagement;

public class mod {
    private String text;
    private int im1, im2;

    public mod(String text, int im1, int im2) {
        this.text = text;
        this.im1 = im1;
        this.im2 = im2;
    }

    public String getText() {
        return text;
    }

    public int getIm1() {
        return im1;
    }

    public int getIm2() {
        return im2;
    }
}
